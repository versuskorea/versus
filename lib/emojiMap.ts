export function getEmoji(text: string): string {
  if (!text || text.trim() === '') return '❓'
  const lower = text.toLowerCase().trim()

  // 정확히 일치
  if (EMOJI_MAP[lower]) return EMOJI_MAP[lower]

  // 포함하는 키워드 (긴 것 먼저 - 우선순위)
  const sorted = Object.entries(EMOJI_MAP).sort((a, b) => b[0].length - a[0].length)
  for (const [keyword, emoji] of sorted) {
    if (lower.includes(keyword)) return emoji
  }

  // 기본값 - 랜덤 중립 이모지 (파란볼 대신 다양하게)
  const defaults = ['⚡', '🔥', '💥', '✨', '🎯', '🌟', '💫', '🎪']
  const idx = lower.charCodeAt(0) % defaults.length
  return defaults[idx]
}
